console.log(2 > 1);
console.log(2 >= 1);
console.log(2 == 1);
console.log(2 != 1);

// String is converted into number
console.log("2" > 1);
console.log("02" > 1);

// null is converted into number as 0
console.log(null > 0);
// equality check and comparison works differently
console.log(null == 0);
console.log(null >= 0);

// === String check : Checks data type as well
console.log("2" === 2);
